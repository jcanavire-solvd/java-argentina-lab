public class Banio extends Sala {

    private int cantidadInodoros;
    private int cantidadMingitorios;

    public int getCantidadInodoros() {
        return cantidadInodoros;
    }

    public void setCantidadInodoros(int cantidadInodoros) {
        this.cantidadInodoros = cantidadInodoros;
    }

    public int getCantidadMingitorios() {
        return cantidadMingitorios;
    }

    public void setCantidadMingitorios(int cantidadMingitorios) {
        this.cantidadMingitorios = cantidadMingitorios;
    }
}
